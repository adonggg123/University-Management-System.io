        menuItems.forEach(function (item) {
            const itemName = item.querySelector("h4").textContent.toLowerCase();
            if (itemName.includes(query)) {
                item.style.display = "flex"; // or "block" depending on your layout
            } else {
                item.style.display = "none";
            }
        });
